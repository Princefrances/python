meter = float(input("Enter an amount for Meter: "))

millimeter = meter * 1000;
centimeter = meter * 100;
decimeter = meter * 10;
kilometer = meter / 1000;
inch = meter * 39.37;
foot = meter * 3.28;
mile = meter / 1609.344;

print("*************************************")
print(meter, "as millimeter is: ",millimeter)
print(meter, "as centimeter is: ",centimeter)
print(meter, "as decimenter is: ",decimeter)
print(meter, "as kilometer is: ",kilometer)
print(meter, "as inch is: ",inch)
print(meter, "as foot is: ",foot)
print(meter, "as mile is: ",mile)
